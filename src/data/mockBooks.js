export function filterBooksByDateRange(books, startDate, endDate) {
  if (!startDate && !endDate) return books;
  const start = startDate ? new Date(startDate) : null;
  const end = endDate ? new Date(endDate) : null;
  return books.filter((book) => {
    const availFrom = new Date(book.availableFrom);
    const availTo = new Date(book.availableTo);
    if (start && end) return start <= availTo && end >= availFrom;
    if (start) return start <= availTo;
    if (end) return end >= availFrom;
    return true;
  });
}
